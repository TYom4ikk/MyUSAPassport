<h1>Лотерея Green Card</h1>

<div class="card">
    <h2>Что такое лотерея Green Card?</h2>
    <p>Diversity Visa Program (DV Lottery) - это ежегодная лотерея грин-карт, проводимая правительством США для граждан стран с низкой иммиграцией. Ежегодно разыгрывается 55,000 грин-карт.</p>
</div>

<div class="card">
    <h2>Основные требования</h2>
    <ul>
        <li><strong>Гражданство:</strong> Вы должны быть гражданином страны, участвующей в лотерее</li>
        <li><strong>Образование:</strong> Среднее образование или 2 года рабочего опыта за последние 5 лет</li>
        <li><strong>Возраст:</strong> Нет ограничений по возрасту</li>
        <li><strong>Здоровье:</strong> Медицинское обследование для иммиграционной визы</li>
    </ul>
</div>

<div class="card">
    <h2>Процесс подачи</h2>
    <ol>
        <li>Регистрация на официальном сайте dvlottery.state.gov (октябрь-ноябрь)</li>
        <li>Проверка результатов (май следующего года)</li>
        <li>Подача документов на иммиграционную визу (после выигрыша)</li>
        <li>Медицинское обследование и интервью</li>
        <li>Получение визы и въезд в США</li>
        <li>Получение грин-карты через 2-3 недели после въезда</li>
    </ol>
</div>

<div class="card">
    <h2>Документы для подачи</h2>
    <ul>
        <li>Паспорт (действительный минимум 6 месяцев)</li>
        <li>Свидетельство о рождении</li>
        <li>Документы об образовании или работе</li>
        <li>Свидетельство о браке (если есть)</li>
        <li>Сертификаты о рождении детей (если есть)</li>
        <li>Фотографии паспортного формата</li>
    </ul>
</div>

<div class="card">
    <h2>Важные моменты</h2>
    <ul>
        <li>Подача заявки абсолютно бесплатна</li>
        <li>Только одна заявка на человека</li>
        <li>Остерегайтесь мошенников, обещающих выигрыш</li>
        <li>Результаты можно проверить только онлайн</li>
        <li>Выигрыш не гарантирует получение визы - нужно пройти собеседование</li>
    </ul>
</div>

