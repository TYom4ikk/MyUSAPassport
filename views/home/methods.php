    <li><strong>Гражданство через брак</strong> — ускоренный путь при браке с гражданином США.</li>
    <li><strong>EB-5 инвестиции</strong> — крупные инвестиции в экономику США.</li>
    <li><strong>Служба в армии США</strong> — отдельные программы для военнослужащих.</li>
