<h1>Брак с гражданином США</h1>

<div class="card">
    <h2>Гражданство через брак</h2>
    <p>Один из самых распространенных способов получения гражданства США - через брак с американским гражданином. Процесс включает получение грин-карты, а затем гражданства.</p>
</div>

<div class="card">
    <h2>Требования к браку</h2>
    <ul>
        <li><strong>Реальный брак:</strong> Брак должен быть настоящим, а не фиктивным</li>
        <li><strong>Возраст:</strong> Оба супруга должны быть старше 18 лет</li>
        <li><strong>Правовой статус:</strong> Никаких ограничений по иммиграционному статусу</li>
        <li><strong>Предыдущие браки:</strong> Предоставление документов о расторжении предыдущих браков</li>
    </ul>
</div>

<div class="card">
    <h2>Процесс получения грин-карты через брак</h2>
    <ol>
        <li>Заключение брака с гражданином США</li>
        <li>Подача петиции I-130 в USCIS</li>
        <li>Подача формы I-485 (Adjustment of Status)</li>
        <li>Биометрия и медицинское обследование</li>
        <li>Собеседование в USCIS</li>
        <li>Получение условной грин-карты (2 года)</li>
        <li>Подача I-751 для снятия условий (за 90 дней до истечения)</li>
        <li>Получение постоянной грин-карты (10 лет)</li>
    </ol>
</div>

<div class="card">
    <h2>Получение гражданства</h2>
    <p>После получения постоянной грин-карты можно подавать на гражданство через 3 года проживания в США (вместо стандартных 5 лет).</p>
    <ul>
        <li>Подача формы N-400 через 3 года</li>
        <li>Сдача теста на английском и истории США</li>
        <li>Собеседование и присяга</li>
    </ul>
</div>

<div class="card">
    <h2>Необходимые документы</h2>
    <ul>
        <li>Свидетельство о браке</li>
        <li>Паспорта обоих супругов</li>
        <li>Свидетельства о рождении</li>
        <li>Доказательства реальности брака (фото, переписка, совместные счета)</li>
        <li>Документы о предыдущих браках (если были)</li>
        <li>Медицинское заключение I-693</li>
        <li>Финансовые документы спонсора</li>
    </ul>
</div>

<div class="card">
    <h2>Важные моменты</h2>
    <ul>
        <li>USCIS тщательно проверяет браки на фиктивность</li>
        <li>Нужно доказывать реальность отношений на протяжении всего процесса</li>
        <li>Совместное проживание обязательно</li>
        <li>Развод в течение 2 лет может привести к депортации</li>
        <li>Процесс может занять 1-2 года</li>
    </ul>
</div>

