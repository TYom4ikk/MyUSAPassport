<h1>Служба в армии США</h1>

<div class="card">
    <h2>Военная служба и гражданство</h2>
    <p>Служба в вооруженных силах США предоставляет ускоренный путь к гражданству для иностранцев. Существуют специальные программы для рекрутирования нерезидентов.</p>
</div>

<div class="card">
    <h2>Основные программы</h2>
    <h3>MAVNI (Military Accessions Vital to the National Interest)</h3>
    <ul>
        <li>Для лиц с критически важными навыками (языки, медицина)</li>
        <li>Быстрое получение гражданства (через 6 месяцев службы)</li>
        <li>Требуется знание английского и специальных навыков</li>
        <li>Программа периодически приостанавливается</li>
    </ul>
    
    <h3>Стандартная военная служба</h3>
    <ul>
        <li>Требуется легальный иммиграционный статус</li>
        <li>Гражданство через ускоренную процедуру</li>
        <li>Сокращенные сроки проживания (от 1 года службы)</li>
        <li>Отсутствие требований к физическому присутствию в США</li>
    </ul>
</div>

<div class="card">
    <h2>Требования к кандидатам</h2>
    <ul>
        <li><strong>Возраст:</strong> 17-35 лет (различается по видам войск)</li>
        <li><strong>Физическая подготовка:</strong> Прохождение стандартных тестов</li>
        <li><strong>Образование:</strong> Минимум среднее образование или GED</li>
        <li><strong>Язык:</strong> Базовое знание английского</li>
        <li><strong>Медицинские требования:</strong> Хорошее здоровье</li>
        <li><strong>Юридический статус:</strong> Легальное пребывание в США (для некоторых программ)</li>
    </ul>
</div>

<div class="card">
    <h2>Процесс получения гражданства через военную службу</h2>
    <ol>
        <li>Контакт с военным рекрутером</li>
        <li>Медицинское обследование (MEPS)</li>
        <li>Тестирование (ASVAB)</li>
        <li>Подписание контракта и присяга</li>
        <li>Базовая подготовка (Basic Training)</li>
        <li>Подача формы N-400 (можно во время службы)</li>
        <li>Ускоренное рассмотрение USCIS</li>
        <li>Присяга на гражданство</li>
    </ol>
</div>

<div class="card">
    <h2>Необходимые документы</h2>
    <ul>
        <li>Паспорт и документы о легальном статусе</li>
        <li>Свидетельство о рождении</li>
        <li>Диплом об образовании</li>
        <li>Медицинские документы</li>
        <li>Свидетельства о браке и рождении детей</li>
        <li>Военные документы после подписания контракта</li>
    </ul>
</div>

<div class="card">
    <h2>Преимущества военного пути</h2>
    <ul>
        <li>Ускоренное получение гражданства</li>
        <li>Образование и профессиональная подготовка</li>
        <li>Медицинское страхование</li>
        <li>Жилье и питание</li>
        <li>Пенсионные benefits</li>
        <li>Помощь в образовании (GI Bill)</li>
    </ul>
</div>

<div class="card">
    <h2>Важные моменты</h2>
    <ul>
        <li>Служба - это серьезное обязательство (4-6 лет)</li>
        <li>Возможность отправки в зону боевых действий</li>
        <li>Высокие физические и психологические требования</li>
        <li>Нужно проходить регулярные медицинские проверки</li>
        <li>Дисциплина и строгая иерархия</li>
    </ul>
</div>

<div class="card">
    <h2>После получения гражданства</h2>
    <p>После получения гражданства через военную службу можно продолжить службу или уйти в запас. Военный опыт дает преимущества при трудоустройстве в гражданских секторах.</p>
</div>

