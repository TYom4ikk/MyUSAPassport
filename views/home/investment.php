<h1>Инвестиции (EB-5)</h1>

<div class="card">
    <h2>Программа EB-5</h2>
    <p>Инвестиционная иммиграционная программа EB-5 позволяет получить грин-карту через инвестиции в американскую экономику. Требуется вложение от $800,000 и создание рабочих мест.</p>
</div>

<div class="card">
    <h2>Основные требования</h2>
    <ul>
        <li><strong>Инвестиция:</strong> $800,000 в Targeted Employment Area или $1,050,000 в других районах</li>
        <li><strong>Рабочие места:</strong> Создание минимум 10 рабочих мест для американских работников</li>
        <li><strong>Источник средств:</strong> Законное происхождение инвестиционных средств</li>
        <li><strong>Возраст:</strong> Нет ограничений по возрасту</li>
        <li><strong>Образование:</strong> Требований к образованию нет</li>
    </ul>
</div>

<div class="card">
    <h2>Типы инвестиций</h2>
    <h3>Прямые инвестиции</h3>
    <ul>
        <li>Создание нового бизнеса</li>
        <li>Полный контроль над инвестицией</li>
        <li>Более высокие требования к управлению</li>
        <li>$1,050,000 минимальная инвестиция</li>
    </ul>
    
    <h3>Региональные центры</h3>
    <ul>
        <li>Инвестиция в утвержденный проект</li>
        <li>$800,000 минимальная инвестиция</li>
        <li>Пассивное участие</li>
        <li>Профессиональное управление</li>
    </ul>
</div>

<div class="card">
    <h2>Процесс получения EB-5</h2>
    <ol>
        <li>Выбор инвестиционного проекта</li>
        <li>Подготовка документов о происхождении средств</li>
        <li>Подача петиции I-526</li>
        <li>Одобрение и условная грин-карта (2 года)</li>
        <li>Создание рабочих мест и выполнение инвестиционных обязательств</li>
        <li>Подача I-829 для снятия условий</li>
        <li>Получение постоянной грин-карты</li>
    </ol>
</div>

<div class="card">
    <h2>Необходимые документы</h2>
    <ul>
        <li>Доказательства законного происхождения средств</li>
        <li>Банковские выписки за 5 лет</li>
        <li>Налоговые декларации</li>
        <li>Документы о собственности бизнесов/недвижимости</li>
        <li>Инвестиционное соглашение</li>
        <li>Бизнес-план проекта</li>
        <li>Личные документы (паспорт, свидетельство о рождении)</li>
    </ul>
</div>

<div class="card">
    <h2>Риски и преимущества</h2>
    <h3>Преимущества</h3>
    <ul>
        <li>Прямой путь к грин-карте</li>
        <li>Нет требований к образованию или опыту</li>
        <li>Возможность для всей семьи (супруга, дети до 21 года)</li>
        <li>Пассивное участие (в региональных центрах)</li>
    </ul>
    
    <h3>Риски</h3>
    <ul>
        <li>Потеря инвестиции</li>
        <li>Длительный процесс (2-5 лет)</li>
        <li>Сложные требования к документации</li>
        <li>Изменения в законодательстве</li>
    </ul>
</div>

<div class="card">
    <h2>Гражданство после EB-5</h2>
    <p>После получения грин-карты через EB-5 можно подавать на гражданство через 5 лет проживания в США. Процесс стандартный - подача N-400, сдача тестов и присяга.</p>
</div>

